export const allIcons = [...new Set(Object.values(iconCategories).flat())];

export function searchIcons(query: string): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return allIcons;
  return allIcons.filter(
    (name) => name.includes(q) || name.replace(/-/g, " ").includes(q)
  );
}

export function findCategory(iconName: string): string {
  for (const [cat, icons] of Object.entries(iconCategories)) {
    if (icons.includes(iconName)) return cat;
  }
  return "Misc";
}
